def main():
    usernames = [u.strip() for u in CB_USERNAMES if u.strip()]
    if not usernames:
        print("❌ Error: No usernames found in the CB_USERNAMES list.")
        sys.exit(1)

    # Hard requirement: cookies must exist before we even start the TUI.
    # This must be a terminal-only error (no TUI flashing).
    if not COOKIES_FILE.exists() or COOKIES_FILE.stat().st_size < 100:
        print("❌ FATAL: Chaturdown_Cookies.txt is missing or empty.")
        print()
        print("   This script cannot run without valid login cookies.")
        print(f"   Expected location: {COOKIES_FILE}")
        print()
        print("Fix:")
        print("   1. Run ./ChaturLogin.py to generate fresh cookies.")
        print("   2. Make sure the file is saved as 'Chaturdown_Cookies.txt' in this same folder.")
        print("   3. Re-run Chaturdown")
        sys.exit(1)

    # 1. Start the API watcher and Downloader in the background
    watcher_thread = threading.Thread(target=watch_loop, args=(usernames,), daemon=True)
    watcher_thread.start()

    # 2. Give the main screen over to the Curses TUI
    curses.wrapper(draw_dashboard)

if __name__ == "__main__":
    try:
        main()
    except KeyboardInterrupt:
        _STOP.set()
    except Exception:
        # Make sure the terminal is usable even if curses is in a bad state
        try:
            curses.endwin()
        except Exception:
            pass
        print("\n💥 The script encountered an unexpected error.")
        print("   A full traceback should appear above (or in your terminal scrollback).")
        print("   Common causes and fixes are documented in the script header and the plan.")
        raise  # Re-raise so the real traceback is still visible
    finally:
        print("\n👋 TUI Closed. Terminal restored cleanly!")
