def get_download_size(target_file: Path) -> int:
    """Calculates the size of the target file using pattern matching."""
    try:
        return sum(f.stat().st_size for f in target_file.parent.glob(f"{target_file.stem}*") if f.is_file())
    except Exception:
        return 0

# ============================================================================
# STALL WATCHDOG
#
# Detects when yt-dlp stops making progress (no stdout for STALL_TIMEOUT seconds).
# On stall we attempt a graceful stop (SIGINT) so the final file is left in a
# consistent state. Because downloads write directly to the final .mkv (no
# separate fragment/mux phase), we no longer perform file-size-based mux
# monitoring after SIGINT.
# ============================================================================
class StallWatchdog:
    def __init__(self, process: subprocess.Popen, target_file: Path):
        self.process = process
        self.target_file = target_file
        self._stopped = False
        self._lock = threading.Lock()
        self.last_output_t = time.time()
        self._thread = threading.Thread(target=self._run, daemon=True)
        self._thread.start()

    def stop(self):
        with self._lock: self._stopped = True

    def ping(self):
        with self._lock: self.last_output_t = time.time()

    def _run(self):
        while True:
            time.sleep(10)
            with self._lock:
                if self._stopped: return
                silence = time.time() - self.last_output_t
            if self.process.poll() is not None: return
            if silence > STALL_TIMEOUT:
                set_api_status("Status: Stall detected — download stopped")
                _terminate_process(self.process, "Stall detected")
                return

def _terminate_process(process: subprocess.Popen, reason: str):
    """Attempt graceful termination of yt-dlp (SIGINT), then hard kill if needed.
    Downloads write directly to the final .mkv file (no separate fragments or post-download mux phase),
    so we no longer monitor file-size growth after SIGINT.
    """
    if process.poll() is not None:
        return
    try:
        process.send_signal(signal.SIGINT)
    except Exception:
        pass

    # Wait up to 30 seconds for clean exit after SIGINT
    for _ in range(30):
        if process.poll() is not None:
            return
        time.sleep(1)

    # Still alive → hard kill
    try:
        process.kill()
    except Exception:
        pass

